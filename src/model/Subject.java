package model;

public class Subject {
	int id;
	String name;
	int subjMark;



public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
public int getSubjMark() {
		return subjMark;
	}
	public void setSubjMark(int subjMark) {
		this.subjMark = subjMark;
	}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}

}
